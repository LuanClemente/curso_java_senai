package atividade_1;
/* *
*Aqui temos que criar as variaveis de tipos diferentes para fazermos os valores
*delas e depois devemos fazer elas serem printadas no terminal
*/
public class atividade1 { //Inicio da classe
     public static void main(String[] args) { //Inicio do metodo main
     int populacao = 100; //Criando variavel do tipo inteiro
     char letra = 'L'; //Criando variavel do tipo caractere (Char)
     boolean ehVerdadeiro = true; //Criando variavel do tipo booleano
     byte idade = 31; //Criando variavel do tipo byte
     short ano = 25; //Criando variavel do tipo short
     long distanciaViaLactea = 105700; //Criando variavel do tipo long
     float preco = 50.00f; //Criando variavel do tipo float
     double pi =  .314159; //Criando variavel do tipo double

     //Acima criamos todas as variaveis

     //Abaixo mostramos todos os valores das variaveis no terminal
     
     System.out.println(populacao + "\n" + letra + "\n" + ehVerdadeiro + "\n" + idade + "\n" + ano + "\n"+ distanciaViaLactea + "\n" + preco + "\n" + pi);
     }//end method main
}//end class
